export function buildChromeLocaleMessages(locale) {
  const values = { ...DEFAULT_MESSAGES, ...(LOCALE_MESSAGES[locale] ?? {}) };
  return Object.fromEntries(
    EXTENSION_MESSAGE_KEYS.map((key) => [
      key,
      {
        message: values[key],
        description: MESSAGE_DESCRIPTIONS[key],
      },
    ]),
  );
}

export async function writeExtensionLocaleFiles(localesDir) {
  await mkdir(localesDir, { recursive: true });
  for (const locale of CHROME_WEB_STORE_LOCALES) {
    const directory = resolve(localesDir, locale);
    await mkdir(directory, { recursive: true });
    await writeFile(resolve(directory, "messages.json"), `${JSON.stringify(buildChromeLocaleMessages(locale), null, 2)}\n`);
  }
}

